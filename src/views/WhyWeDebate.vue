<template>
  <div class="article-page">
    <div class="article-container">
      <h1 class="article-title">我们为何辩论</h1>
      <blockquote class="source-quote">
        <p>本文内容整理于黄执中先生在 TED 的演讲：《辩论是一个重新认识世界的过程》。</p>
      </blockquote>
      <p class="article-body">
        <span class="drop-cap">我</span>从初中的时候开始接触辩论，到现在为止，我打了20几年辩论。对我而言，辩论不只是一个所谓说话的技巧，他对我提供的是一整套思考问题的方式，以及看待事情的角度。今天在这个有限的时间里，我非常希望能够在最快的点当中，给大家介绍一点点，我在辩论当中所获得的，它对我造成的最大的改变，以及最有贡献的价值的释放。
      </p>
      <p class="article-body">
        我知道，对各位而言，可能辩论的部分主要表现在语言上。可是我们不是。对我而言，辩论是一个重新认识世界的过程。这在我一开始接触辩论的时候，最震撼的是这一点。什么意思呢？这句话听起来很复杂，其实并一点都不会。在座各位，你们都认识世界，你左顾右盼，你在这里头的所见到的东西都认识：椅子啊、布景、灯光、皮鞋、讲者你都认识。可是你真的认识，你以为你认识的东西吗？
      </p>
      <p class="article-body">
        你们先假想一下，假设我是一个外星人，我来到地球，我地球上的东西，我一样东西都不认识。我需要借由你们告诉我，你们认识这个世界，我不认识你们的世界，请你们告诉我什么是桌子？第一个，木头做的。这就就太奇特了，对不对？这我们有看过各种材质的桌子：石头做的、钢铁做的、玻璃做的、塑胶做的。第二个是放置物品的工具。你们现在所放置屁股的这个工具，一定不叫做桌子吧？它也符合柜子，也符合架子，它符合床铺。所以我用比较戏虐的方法回应你们，可是你们完全知道你们所讲的答案，并不是真正的桌子。
      </p>
      <p class="article-body">
        在座的各位，我相信你们每一个人长到这么大，你们都一定知道什么是桌子。可是我们不知道。我们不知道，你知道为什么我们不知道我们不知道吗？因为当你小的时候问老师“老师什么是桌子”，当你这辈子第一次认识这个世界，“老师，什么是桌子”的时候，老师说：“哎呀，搭配椅子就是桌子。”哦，我懂了。你懂个六，你啥都不懂。当你第一次问老师“老师什么是桌子”，“哎呀，木头做的，摆东西的哦”，我了解了。你了解个西瓜，你什么都不了解。可是你那时候说，“哦，我懂了”。为什么？我们大多数人对这个世界的认识，都是在一种妥协以及不追究的情况下，所达成的结论。这一辈子大概不会有人像我刚才这样，有人真的在追究“你知道什么是桌子吗”，然后讲完答案，“这个答案合理吗”，下一个答案，“这个答案合理吗”。简言之，我在干嘛？辩论是你这辈子第一次，可能是第一次用辩论的态度来跟你追究，你到底知不知道什么是桌子的人。而你那一刻你发觉，哦，我不知道啊，我不知道。而在这一刻，你才开始了你的第二次认识世界的过程。你知道了，你不知道，你才有可能去知道。
      </p>
      <p class="article-body">
        而且各位，我现在跟大家聊的是最简单的东西：什么是桌子，什么是笔，什么是教室。还没完。我们也都知道，我们人生当中，我们接触更多，尤其在辩论当中，跟人家讨论问题当中，更多更了不起的词句。比如说什么叫公平。我们常常会讲：“这件事情对我不公平”，“我已经对你很公平了”，“这样对大家公平吗”。我们常讲话来，当你在用这些词的时候，什么叫公平？你连什么是桌子都答不-出来，你连什么是公平都不知道。然后我们大多数的时候，用一个我们不是很熟的词，就可以侃侃而谈这件事，公平或不公平。这对打辩论的而言，这对打辩论的人而言，这个过程是非常奇妙的。而辩论给我一个好处，他给我一个练习，他给我一个重新认识世界的方式。每个打辩论的人都在思考一个问题时，当然，我们不会在日常生活对话对人家这么不礼貌。日常生活当中，如果你告诉我什么是桌子，“哎呀，木头做了摆东西的”，我说：“哦，棒棒的啊”。因为我们日常生活，我们不需要用辩论的态度来面对。可是我脑中会知道啊，我脑中会知道。可是我因为我们的教养跟礼貌，我们会把它躲起来。可是我们自己会知，这个答案不对劲。而这个练习跟这个素养对我而言，帮助非常大。他让我等于是回到了幼稚园，重新指认一，我以为我知道的东西。而这一次，幼稚-园老师没那么好糊弄我。
      </p>
      <p class="article-body">
        另外，什么是辩论？辩论是一个不断寻找观点的过程。什么叫观点的过程？我们在打辩论的时候，很多人会说这是一个误会。他说：“哇塞，你们打辩论的人，你们谈论各种各样的问题，谈婚姻、谈爱情、谈家庭、谈养老啊，谈城市、谈歧视，你们怎么什么都知道？你们一定读了很多书。”这是一个美丽的误会。我们在辩论的时候，或者是学辩论的人，他真正巨大的武器不是知识点，是观点。我们怎么可能与那些真正的学者，了不起的教授比肩？我们没有那么自大，也绝对不敢这样狂妄。人家说需要你看书、看很多，我说多是多比你多，可是绝对没有到真正意义上的多。我们绝对不敢自己在这个地方，有特别丰厚的学养。可是为什么我们能够对一件事情，发表各种看法？其中一个原因是因为那跟知识点无关，那跟观点有关。
      </p>
      <p class="article-body">
        什么叫做知识点？知识点大家都知道是什么，怎么做，为什么这叫知识点。来，我跟各位报告一个知识点啊，这个什么蟑螂啊，一旦切掉头之后呢，他还是可以活多久啊，这叫知识点。什么叫观点？顾名思义，观点是你看一件事情，你从哪个点去观察它，你看它的角度。我在课堂上常常举这个例子。好，你们在纸上给我画一个水平。班上同学会怎么画？你们会怎么画？在我个人的经验中，如果100个班上同学，99.999的，他最后画出来的水平就是像这样，就是一张纸，画一个水平。我从来没有看过，老师要你画一个水平的时候，你画的是这个样。可是这样是不是一个水平啊？这也是一个水平。有没有人这样在纸上这样画？这是不是一个水平？这也是一个水平。有没有人这样画？没有人这样画。可这是不是一个水平？为什么？当我们一想到脑筋浮现一个水平的时候，他就是这样。听得懂吧？它永远都是这样。甚至，如果我们把它捏了，这是不是一个水平？这也是一个水平。有没有人这样画？没有人这样画。大家对于水平的观察的角度只有一个，就是这样的一个。这叫做一个水平。我们的观点看事情的东西，非常地直接而统一。而这个直接而统一的程度，超出有时候超出我的想象。当你换一个角度去看水平的时候，并没有增加任何的知识点。水平还是在这，它的体积密度什么都没改变，可是你观察它的方法改变了。只有这一点改变。
      </p>
      <p class="article-body">
        我印象很深的，台湾有一位我小时候非常特别喜欢的一位作家叫刘墉，他本身也是一位画家。我看过有一次，他谈谈到他担任绘画比赛的评审的故事。他说啊，他那时候担任一个小学生，户外写生比赛的评审。写生什么呢？划龙舟啊。一堆小孩子在看龙舟比赛，然后最后画在纸上。有没有看过小孩子画划龙舟？一张中间一条水平线啊，有的水平线高，有的水平线低，不是重点。水平线上画一条船，有的画的好，有的画的坏，也不是重点。船上画火柴棒的小人拿着奖，这就是划龙。水平线下面就是岸边咯，画很多像葡萄一样的黑色的球，那是什么后脑勺啊。很多的观众有人画的多，有人画的少，也不是重点。天上画了云，旁边画一个圆圈像个气球，长了毛就是太阳。400多篇作品当中，几乎全部都是这样。他一篇一篇作品看，他看到了其中一篇，他看的那一刹那，就知道这个小孩一定冠军，一定就是这个写生比赛冠军。为什么？因为在400多篇作品里头，只有这位小孩，他他的龙舟是在画面上这样开过来的，对着你观画者这样开过来。所有龙舟都是跟你的视线呈垂直，这样子过去的。只有他是这样过来的。只有他想到，从那个角度来画水平，来划龙舟。他说这当他找到了这个角度的时候，他的画的技法、配色通通都不重要。知道吗？因为当你有一个独特的角度之后，你的一，画的笔法熟练与否、配色允荡与否、细节精细琢与否，那都叫风格。角度对了，画的好坏都叫风格。他说只有他知道这样，只有他想到了这样看龙舟。
      </p>
      <p class="article-body">
        不过我帮你往下再延伸一下。你们想想看，这个小孩所画的这个划龙舟，他那个迎着面来的，这有可能是这个小孩亲眼所见的结果吗？不可能，对不对？你自己试想嘛，龙舟迎面而来，你在写生，请问你在哪啊？你在河中央。所以这不是她真正看到的，是她想象我可以从这个角度看到的。相反的，所有其他400多位作品，那个龙舟这样横着走，这个这个小孩子，我问你，他们画的都是他们看到的吗？其实反而不可能。为什么？因为一定有人做得高，有人做的低嘛。龙舟，你在户外看的时候对不对？一定有人是斜着看，有人是高的看，有人是矮的看嘛。可是你明明看到的角度都不一样，画到纸上全部一样，90度垂直。他脑中有一个那些小孩，脑中有一个机器，有一个机制，把他看到明明不一样的角度，统合成唯一的视角。而那个最棒的那个小朋友，则有另一个机智，把他没有看到的视，怎么样想象出来。而这一来一回，差距就天差地远。
      </p>
      <p class="article-body">
        那你说，我们为什么要学这个呢？你你你讲的很有道理，打辩论可以帮助我们，用不同的视角来看问题。可是我们又不会去画划龙舟，我们也不会去画水平，有什么用处呢？有用处。我印象很深的是，有一次有一个做业务的朋友，他举手，他说：“老师，我想问一下，我现在遇到一个问题，叫做我的客户不信任我该怎么办？”那你就衣着得体一点喽，讲话有权威感一点咯，笑容甜一点咯。可是来我自己在学辩论的时候，我们永远先思考一件事，叫做这是谁的问题。
      </p>
      <p class="article-body">
        什么叫这是谁的问题？不是说谁出了问题，而是这个问题可以是谁的问题。你想想看，“我的客户不信任我该怎么办？”那位朋友第一想法就是我该怎么办嘛，因为这是我的问题嘛。有没有可能这个问题是属于客户的问题，是一个困扰他的问题呢？比如说我们有没有可能是这样说的一件事情：“王先生呐，不好意思，在担任您业务的过程当中，我发觉您非常的不信任我。我觉得这个问题你要重视一下好，因为由于你对我的不信任，以至于我没有办法提供最好的服务，也会让我们在合作关系当中，增加很大的成本。所以王先生，你真的要想一-想办法，你这样子对业务的不信任了，这个问题你要怎么解决？你要好好思考一下。”听得懂吗？这是谁的问题？并不是固定的，是随着你观察视角的不同，它是可以移动的。
      </p>
      <p class="article-body">
        所以他可以是我的问题，可以是王先生这位客户，你的问题还可以是谁的问题？老板呐，我每次穿着我们公司的制服出去，我的客户都不信任我哈，我的客户都不信任我。这对我的推销或者是对我的推广而言，造成很大的困扰。所以我觉得我的客户不信任我，公司该怎么办？这是谁的问题？公司的问题。这不是我的问题，这不是王先生问题，公司你要想想。我已经尽我力量了，当然我可以在我这边更加一把劲，可是这累积起来的成本，浪费的时间跟心力，其实也是转嫁在公司身上嘛。所以公司要想想怎么帮我们解决这个问题。公司现在面临一个很大的危机，你的手下出去不被信任。这是另一种思路。
      </p>
      <p class="article-body">
        所以我有没有说过，同一件事对我而言，最大的两个收获就在，重新认识世界，以及不断寻找看问题的角度。也就是说一个问题，它的解决方法就不会只有一条路。要理解一件事情，认同一件事情或改变一件事情的方法，一旦他的路线变多了，人们被说服、被改变，跟重新思考的可能性就会加大。这就是为什么我一开始所说的辩论，关键不在于知识点。当然知识很重要，可是关键不在知识点在什么观点。好有限的时间分享到这边，谢谢大家。
      </p>
